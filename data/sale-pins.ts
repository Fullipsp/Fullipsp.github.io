export interface Pin {
  src?: string
  title?: string
  subtitle?: string;
  status?: PreviewType
  gap?: boolean;
}

export interface Category {
  title: string
  url?: string
  pins: Pin[]
}


function category  (title: string, url?: string) {
  setCategories((prev) => [...prev, { title, url, pins: [] }])
  const index = categories.length - 1


  const pin = (src: string, title: string, subtitle: string, status: PreviewType) => {
    setCategories(index, "pins", (prev) => [...prev, { src: `/pins/${src}`, title, subtitle, status }])
    return {pin, gap}
  }  
  const gap = () => {
    setCategories(index, "pins", (prev) => [...prev, {gap: true }])
  }
  return {
    pin,
    gap
  }
}

export const pinCategories = () => {
  return categories;
}
