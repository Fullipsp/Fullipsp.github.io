export interface Pin {
  src?: string
  title?: string
  subtitle?: string;
  status?: PreviewType
  gap?: boolean;
  crop?: `${number} ${number} ${number} ${number}`
}

export interface Category {
  title: string
  url?: string
  pins: Pin[]
}


function category  (title: string, url?: string) {
  setCategories((prev) => [...prev, { title, url, pins: [] }])
  const index = categories.length - 1


  const pin = (src: string, title: string, subtitle: string, status: PreviewType, crop?: `${number} ${number} ${number} ${number}`) => {
    setCategories(index, "pins", (prev) => [...prev, { src: `/pins/${src}`, title, subtitle, status, crop }])
    return {pin, gap}
  }
    const gap = () => {
    setCategories(index, "pins", (prev) => [...prev, {gap: true }])
  }
  return {
    pin,
    gap
  }
}

export const pinCategories = () => {
  return categories;
}
